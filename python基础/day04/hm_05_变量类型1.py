"""
变量类型：
    数值型：
        整型
        浮点型
        布尔型
        复数型：主要用于科学计算
    非数字型：
        字符串
        列表
        元组
        字典

    type函数可以查看数据类型
"""
name = "小明"
age = 18
height = 1.75
genter = True
print(type(name))
print(type(age))
print(type(height))
print(type(genter))

print(type(2 ** 32))
print(2 ** 1000)
print(type(2 ** 1000))
