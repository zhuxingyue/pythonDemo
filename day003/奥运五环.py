import turtle

turtle.showturtle()

turtle.circle(100)

turtle.penup()
turtle.goto(200,0)
turtle.pendown()

turtle.color("blue")
turtle.circle(100)

turtle.penup()
turtle.goto(-200,0)
turtle.pendown()

turtle.color("red")
turtle.circle(100)

turtle.penup()
turtle.goto(100,-200)
turtle.pendown()

turtle.color("yellow")
turtle.circle(100)

turtle.penup()
turtle.goto(-100,-200)
turtle.pendown()

turtle.color("green")
turtle.circle(100)

turtle.done()